public class ejercicio_15 {

    /*
    *. Programa que calcula el área de un triángulo a partir de la longitud de sus lados.
    * */
    int longitudB=3;
    int longitudH=5;
    float area;
    public float calcularAreaTriangulo()
    {
       this.area=(float)(this.longitudB*this.longitudH)/2;

       return this.area;
    }
}
